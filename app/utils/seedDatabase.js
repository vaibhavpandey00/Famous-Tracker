// Utility function to calculate the maximum follower count from an array of social objects.
function calculateMaxFollower(socials) {
    if (!socials || socials.length === 0) {
        return null;
    }

    const followerCounts = socials
        .map(s => s.followers)
        .filter(f => typeof f === 'number' && f !== null);

    if (followerCounts.length === 0) {
        return null;
    }

    return Math.max(...followerCounts);
}

// Utility function to format follower count into K/M/B string.
function formatFollowerCount(num) {
    if (num === null) {
        return null;
    }
    // Using \\.0$ to escape the dot for regex and .0 for precision in toFixed()
    if (num >= 1000000000) {
        return (num / 1000000000).toFixed(1).replace(/\\.0$/, '') + 'B';
    }
    if (num >= 1000000) {
        return (num / 1000000).toFixed(1).replace(/\\.0$/, '') + 'M';
    }
    if (num >= 1000) {
        return (num / 1000).toFixed(1).replace(/\\.0$/, '') + 'K';
    }
    return String(num);
}

